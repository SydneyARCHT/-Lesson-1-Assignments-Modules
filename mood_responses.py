def mood_response(mood):
    if mood == "happy":
        return "Great! It's going to be a great day! "
    elif mood == "sad":
        return "Let's turn that frown upside down! "
    elif mood == "excited":
        return "On top of the world, are we? "
    else:
        return "Haven't heard of that mood... "
    
